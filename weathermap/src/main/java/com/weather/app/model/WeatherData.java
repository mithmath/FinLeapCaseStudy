package com.weather.app.model;

public class WeatherData {

	private String dayTemparature;
	private String nightTemparature;
	private String preasure;

	public String getDayTemparature() {
		return dayTemparature;
	}

	public void setDayTemparature(String dayTemparature) {
		this.dayTemparature = dayTemparature;
	}

	public String getNightTemparature() {
		return nightTemparature;
	}

	public void setNightTemparature(String nightTemparature) {
		this.nightTemparature = nightTemparature;
	}

	public String getPreasure() {
		return preasure;
	}

	public void setPreasure(String preasure) {
		this.preasure = preasure;
	}

}
